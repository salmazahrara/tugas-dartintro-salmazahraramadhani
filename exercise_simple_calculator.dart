void main() {
  double a = 100;
  double b = 3;

  print('Angka 1       : $a');
  print('Angka 2       : $b');
  print('Penjumlahan   : ${a + b}');
  print('Pengurangan   : ${a - b}');
  print('Perkalian     : ${a * b}');
  print('Pembagian     : ${a / b}');
}

// + = PERTAMBAHAN
// - = PENGURANGAN
// * = PERKALI
// / = PEMBAGIAN --> TIPENYA DOUBLE 
// ~/ = PEMBAGIAN HASILNYA INT
// % SISA BAGI/ MODULO
